    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM tbl_pelanggan WHERE telepon = '" + tfTel.getText() + "' AND '"
                    + tfNampel.getText() + "'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                insertTransaksi();
            } else {
                insertPelanggan();
                insertTransaksi();

            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_btSimpanActionPerformed

    private void insertTransaksi() {
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "INSERT INTO tbl_transaksi (no_transaksi, total_bayar, id_user, nama, id_barang) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, "TR-" + tfTotalHarga.getText()+id_user+tfTotalHarga.getText());
            pst.setString(2, tfTotalHarga.getText());
            pst.setInt(3, id_user);
            pst.setString(4, tfNampel.getText());

            pst.setString(5, tfMenu.getText());

            pst.executeUpdate();
            System.out.println("Data berhasil ditambahkan!");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void insertPelanggan() {
        try {
            Connection conn = Koneksi.getConnection();
            String sql = "INSERT INTO tbl_pelanggan (nama, telepon) VALUES (?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, tfNampel.getText());
            pst.setString(2, tfTel.getText());
            pst.executeUpdate();
            System.out.println("Data berhasil ditambahkan!");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

