    private void getData() {

        try {
            String[] columnName = { "ID", "Kode", "Nama Barang", "Tanggal", "Qty", "Satuan", "Harga" };

            DefaultTableModel model = (DefaultTableModel) tbLog.getModel();
            model.setColumnIdentifiers(columnName);
            model.setRowCount(0);

            tbLog.setEnabled(true);
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM tbl_barang";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[] { rs.getString("id_barang"), rs.getString("kode_barang"),
                        rs.getString("nama_barang"),
                        rs.getString("expired_date"), rs.getString("jumlah_barang"), rs.getString("satuan"),
                        rs.getString("harga_satuan") });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

