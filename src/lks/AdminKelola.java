    private void getData() {

        try {
            String[] columnName = { "No", "Nama", "Peran", "Alamat", "Telepon" };

            DefaultTableModel model = (DefaultTableModel) tbLog.getModel();
            model.setColumnIdentifiers(columnName);
            model.setRowCount(0);

            tbLog.setEnabled(true);
            Connection conn = Koneksi.getConnection();
            String sql = "SELECT * FROM tbl_user";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[] { rs.getString("id_user"), rs.getString("nama"), rs.getString("tipe_user"),
                        rs.getString("alamat"), rs.getString("telepon") });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

