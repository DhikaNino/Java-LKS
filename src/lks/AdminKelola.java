    public void filterData(String query) {
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>((DefaultTableModel) tbLog.getModel());
        tbLog.setRowSorter(sorter);

        // Jika query kosong, tampilkan semua data
        if (query.isEmpty()) {
            sorter.setRowFilter(null); // Tampilkan semua data
        } else {
            // Filter berdasarkan query, case-insensitive
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }

